public class Lab1Part2 {

    public static void main(String args[]) {

    	//create array
    	String myArray[] = {"J","o","s","e","p","h"};

    	//for loop to print array
    	for(int i = myArray.length-1;i>=0;i--){
    		System.out.print(myArray[i]);
    	}//end for loop
    	System.out.print("\n\n");
    }//end main

}//end class